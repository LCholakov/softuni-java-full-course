package Week3_Conditionals2.More;

import java.util.Scanner;

public class NumbersFrom1to100_09
{
    public static void main(String[] args)
    {
        Scanner scanner = new Scanner(System.in);

        int x = 1;
        while (x <= 10)
        {
            System.out.printf("%d\n", x);
            ++x;
        }
    }
}